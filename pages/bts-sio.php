    <h1>Qu'est ce que le BTS Services Informatiques aux Organisations ? </h1>
    <p>
        Le BTS Services Informatiques aux Organisations est un diplôme reconnu par l’état de niveau BAC+2. <br>
        Il est habituellement réalisé sur deux ans, il est néanmoins possible de le réaliser en un an dans le cadre d'une reconversion professionnelle au sein du GRETA de Vannes.<br><br>
        Ce diplôme se divise en deux options : </p><br>
    <ul>
        <li>Option Solutions Logicielles et Applications Métiers (SLAM)</li>
        <li>Option Solutions d'Infrastructures Systèmes et Réseaux (SISR)</li>
    </ul>

    <!-- Options du bts -->
    <h1>Les options</h1>
    <div class="options-container">
        <div class="slam">
            <h2>Option SLAM</h2>
            <hr>
            <p>Orientée <span class="important-word">développement</span>.<br>
                Elle permet d’apprendre la <b>conception</b>, le <b>développement</b>, ainsi que le <b>déploiement</b> et la <b>maintenance</b> des composants logiciels d'une solution applicative, mais aussi la <b>gestion des bases de données</b>.
                <br>Formation à la <b>cybersécurité</b> d'une solution applicative et de son développement.
            </p>
        </div>
        <div class="sisr">
            <h2>Option SISR</h2>
            <hr>
            <p>Orientée <span class="important-word">réseaux</span>.<br>
                Elle permet d’apprendre un métier qui consiste à <b>installer</b>, <b>configurer</b> et <b>gérer</b> les équipements et les réseaux informatiques.
                Cela signifie intervenir sur la <b>conception</b>, la <b>mise en place</b>, le <b>déploiement</b> et la <b>maintenance</b> des programmes destinés à la gestion au sein d’une organisation, avec un œil également du côté de la <b>cybersécurité</b></p>
        </div>
    </div>
    <div class="commun">
        <!-- parcours commun du bts -->
        <h1>Les modules communs aux deux spécialitées</h1>
        <h4>Support et mise à disposition de services informatiques</h4>
        <p>Il s'agit de <b>répondre aux attentes des utilisateurs</b> en assurant la <b>disponibilité</b> des services informatiques existants, de <b>prendre en compte les besoins informatiques</b> dans l'entreprise et d'<b>accompagner</b> la transformation numérique des services informatiques.
            <br>Les compétences suivantes sont également apprises :
        </p><br>
        <ul>
            <li>gérer le <b>patrimoine informatique</b></li>
            <li>répondre aux <b>incidents</b></li>
            <li>développer la <b>présence de l'entreprise sur le Web</b></li>
            <li>organiser son <b>propre développement professionnel</b></li>
        </ul>

        <h4>Cybersécurité des services informatiques</h4>
        <p>Apprentissage des compétences sur la <b>protection des données</b> et de l'identité numérique de l'entreprise, la <b>sécurisation</b> des équipements et usages des utilisateurs.</p>
